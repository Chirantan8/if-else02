void main() {
  int x = 2; 
  switch (x) {
    case 1:
      print('Violet');
      break;
    case 2:
      print('Indigo');
      break;
    case 3:
      print('Blue');
      break;
    case 4:
      print('Green');
      break;
    case 5:
      print('Yellow');
      break;
    case 6:
      print('Orange');
      break;
    case 7:
      print('Red');
      break;
    default:
      print('Invalid input');
  }
}
